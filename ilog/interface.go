package ilog

import (
	"context"
	"log"
)

type StdLogger interface {
	Print(...interface{})
	Printf(string, ...interface{})
	Println(...interface{})

	Fatal(...interface{})
	Fatalf(string, ...interface{})
	Fatalln(...interface{})

	Panic(...interface{})
	Panicf(string, ...interface{})
	Panicln(...interface{})
}

type Logger interface {
	StdLogger
	Raw() *log.Logger
	WithContext(ctx context.Context) StdLogger
}
